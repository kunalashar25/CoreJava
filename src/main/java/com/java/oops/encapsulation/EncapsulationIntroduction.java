package com.java.oops.encapsulation;

public class EncapsulationIntroduction {

	// 1. The process of binding data and corresponding methods into a single unit is known as Encapsulation.

	// Example:

	private int rollNo;

	public int getRollNo() {
		return rollNo;
	}

	public void setRollNo(int rollNo) {
		this.rollNo = rollNo;
	}

	// 2. If any component follows, data hiding and abstraction. Such type of component is said to be Encapsulated
	// Component.

	// Encapsulation = Data Hiding + Abstraction

	// 3. The main advantages of Encapsulation are:
	// - We can achieve Security.
	// - Enhancement will be easy.
	// - It improves maintainability of the application.

	// 4. Main disadvantage of encapsulation is it increases length of the code and slows down execution.

}
