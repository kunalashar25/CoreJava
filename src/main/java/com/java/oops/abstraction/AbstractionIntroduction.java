package com.java.oops.abstraction;

public class AbstractionIntroduction {
	// 1. Hiding internal implementation and just highlighting the set of service which are offered is concept of
	// Abstraction.

	// Example: Through Bank ATM GUI screen, bank people are highlighting the set of services which they're offering
	// without highlighting internal implementation.

	// 2. The main advantages of abstraction are:
	// - We can achieve security because we're not highlighting our internal implementation.
	// - Without affecting end user, we can perform any type of changes in our internal system. Hence, enhancement will
	// become easy.
	// - It improves maintainability of the application.
	// - It improves easiness to use our system

	// 3. By using interfaces and abstract classes, we can implement abstraction.

}
