package com.java.codeSnippets.literals;

public class Snippet05 {

	public static void main(String[] args) {
		char ch1 = 'a';
		char ch2 = 'J';
		char ch3 = '5';
		char ch4 = '*';

		System.out.println(ch1); // Output: a
		System.out.println(ch2); // Output: J
		System.out.println(ch3); // Output: 5
		System.out.println(ch4); // Output: *
	}

}
