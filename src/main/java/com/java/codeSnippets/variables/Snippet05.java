package com.java.codeSnippets.variables;

public class Snippet05 {

	public static void main(String[] args) {
		int a = 99;
		boolean b = true;
		double d = 99.99;
		String s = "Hi";

		System.out.println(a); // Output: 99
		System.out.println(b); // Output: true
		System.out.println(d); // Output: 99.99
		System.out.println(s); // Output: Hi

	}

}
