package com.java.literal;

public class StringLiteral {

	public static void main(String[] args) {
		// any sequence of chars within double quotes is treated as string
		// literal
		String s = "Demo";

		// Enhancements in Java 1.7
		// Binary Literals
		// For intgeral data types
		int x = 0B1111;
		System.out.println(x);
	}

}
